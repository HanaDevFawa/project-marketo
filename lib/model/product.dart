class Product {
  final String id;
  final String name;
  final double price;
  final String brand;
  final String description;
  final String shopName;
  final String category;
  final String quantity;
  final double rating;
  final String imageUrl;

  const Product(this.id, this.name, this.price, this.brand, this.description, this.shopName, this.category,
      this.quantity, this.rating, this.imageUrl);
}
